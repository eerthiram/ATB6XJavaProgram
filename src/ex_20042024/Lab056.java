package ex_20042024;

public class Lab056 {
    public static void main(String[] args) {
        //Ternary operator(if the first condition is true returns the second value, else 3rd value)
        boolean a=(30>40) ? true : false;
        System.out.println(a);

        int z=(40<20) ? 0 : 1;
        System.out.println(z);

    }
}
