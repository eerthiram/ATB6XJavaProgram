package ex_20042024;

public class Lab057 {
    public static void main(String[] args) {
        //Ternary operator (a?b:c)
        int a=20;
        int b=30;
        int y_min = (a<b) ? a : b;
        System.out.println(y_min);
        int y_max = (a>b) ? a : b;
        System.out.println(y_max);

    }
}
