package ex_02052024arrays;

public class Lab102 {
    public static void main(String[] args) {
        //ways to create array in java.
        int a[]={1,2,3};
        System.out.println(a.length);
        //print all the elements inan array
        for(int i=0;i<a.length;i++)//If we are putting <= in for loop 'i' should start from i=1.
        {
            System.out.println(a[i]);
        }
    }
}
