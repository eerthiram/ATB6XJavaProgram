package ex_04052024array2;

public class Lab112 {
    public static void main(String[] args) {
        int[] arr={24,25,26};
        //for each loop for printing arrays
        for(int item:arr){
            System.out.println(item);
        }

        for(int i=0;i<arr.length;i++){
            System.out.println("arr[i]: "+arr[i]);
        }
    }
}
//Note For each loop is more simpler than for loop and easy for printing array elements.

